using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.Audio;

// Developer ~ ?

public class AudioLevels : MonoBehaviour
{
	//----------------------------------------------------------------------------------------------------------------------------------
    // Variables
	
	public AudioMixer theAudioMixer;
	
	//----------------------------------------------------------------------------------------------------------------------------------
	
	
	
	
	//----------------------------------------------------------------------------------------------------------------------------------
	// Method | 
	public void ChangeMasterVol(float masterVol)
	{
		theAudioMixer.SetFloat("masterVol", Mathf.Log10(masterVol) * 20);
	}
	//----------------------------------------------------------------------------------------------------------------------------------
	
	
	
	
	//----------------------------------------------------------------------------------------------------------------------------------
	// Method | 
	public void ChangeMusicVol(float musicVol)
	{
		theAudioMixer.SetFloat("musicVol", Mathf.Log10(musicVol) * 20);
	}
	//----------------------------------------------------------------------------------------------------------------------------------
	
	
	
	
	//----------------------------------------------------------------------------------------------------------------------------------
	// Method | 
	public void ChangeSfxVol(float sfxVol)
	{
		theAudioMixer.SetFloat("sfxVol", Mathf.Log10(sfxVol) * 20);
	}
	//----------------------------------------------------------------------------------------------------------------------------------
}